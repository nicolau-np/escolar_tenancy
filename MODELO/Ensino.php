<?php

class Ensino {
    
    private $id_ensino;
    private $ensino;
    
    function getId_ensino() {
        return $this->id_ensino;
    }

    function getEnsino() {
        return $this->ensino;
    }

    function setId_ensino($id_ensino) {
        $this->id_ensino = $id_ensino;
    }

    function setEnsino($ensino) {
        $this->ensino = $ensino;
    }




}
