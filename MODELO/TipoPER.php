<?php

class TipoPER {
    
    private $id_tipopermicao;
    private $tipo;
    
    function getId_tipopermicao() {
        return $this->id_tipopermicao;
    }

    function getTipo() {
        return $this->tipo;
    }

    function setId_tipopermicao($id_tipopermicao) {
        $this->id_tipopermicao = $id_tipopermicao;
    }

    function setTipo($tipo) {
        $this->tipo = $tipo;
    }





}
